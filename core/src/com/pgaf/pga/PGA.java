	public static SpriteBatch batch;

	/* WARNING Using AssetManager in a static way can cause issues, especially on Android.
	Instead you may want to pass around Assetmanager to those the classes that need it.
	We will use it in the static context to save time for now. */
	public static AssetManager manager;
	
	@Override
	public void create () {
		batch = new SpriteBatch();
		manager = new AssetManager();
		manager.load("audio/music/pumptheme.ogg", Music.class);
		manager.load("audio/pumpsound/stomp1.wav", Sound.class);
		manager.load("audio/pumpsound/powerup1.wav", Sound.class);
		manager.load("audio/pumpsound/powerdown.wav", Sound.class);
		manager.load("audio/pumpsound/pumpdie.wav", Sound.class);
		manager.load("audio/pumpsound/jump.wav", Sound.class);
		manager.finishLoading();

		setScreen(new MainMenu(this));

	}

	@Override
	public void dispose() {
		super.dispose();
		manager.dispose();
		batch.dispose();
	}

	@Override
	public void render () {
		super.render();
	}



